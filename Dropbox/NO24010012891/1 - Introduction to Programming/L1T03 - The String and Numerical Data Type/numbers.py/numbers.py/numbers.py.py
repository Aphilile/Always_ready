# Ask the user to enter three different integers
num1 = int(input("Enter the first integer: "))
num2 = int(input("Enter the second integer: "))
num3 = int(input("Enter the third integer: "))

#printing by adding the sum of all numbers
print("Sum of all numbers:", num1 + num2 + num3)

#printing the first number minus the second number
print("First number minus the second number:", num1 - num2)

#printing the third number multiplied by first number
print("Third number multiplied by the first number:", num3 * num1)

#printing the sum of all numbers and divide them by third number
print("Sum of all three numbers divided by the third number:", (num1 + num2 + num3) / num3)