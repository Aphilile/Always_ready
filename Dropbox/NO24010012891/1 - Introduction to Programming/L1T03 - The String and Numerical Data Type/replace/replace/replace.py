#Defining the sring with exclamation mark
file_string = "The!quick!brown!fox!jump!over!lazy!dog"

#Remove the exclamation mark on the string
file_string = file_string.replace("!", " ")

#printing the string without exclamation mark
print(file_string)

#write the string in upper cases
file_string_upper_case = file_string.upper()

#printing the string with upper cases
print(file_string_upper_case)

#Reverse the words from backwards until the first word of the string
reversed_string = file_string[::-1]

#Printing the string in reverse
print(reversed_string)


 
